export function Main() {
  const [sorting, setSorting] = React.useState<SortingState>([])
  const [precoGasolina, setPrecoGasolina] = React.useState<string>("")

  // Carregar o preço da gasolina via fetch
  React.useEffect(() => {
    async function fetchGasPrice() {
      const response = await fetch("/path/to/gas_price.json")  // Altere o caminho conforme necessário
      const data = await response.json()
      setPrecoGasolina(data.preco_gasolina)
    }

    fetchGasPrice()
  }, [])

  // Atualizar o dado com o preço da gasolina
  const updatedData = data.map((payment) => ({
    ...payment,
    preco_gasolina: precoGasolina,  // Preencher com o valor do JSON
  }))

  const table = useReactTable({
    data: updatedData,
    columns,
    onSortingChange: setSorting,
    getCoreRowModel: getCoreRowModel(),
    getPaginationRowModel: getPaginationRowModel(),
    getSortedRowModel: getSortedRowModel(),
    state: {
      sorting,
    },
  })

  return (
    <div className="w-full">
      {/* Renderização da tabela */}
      <Table>
        <TableHeader>
          {table.getHeaderGroups().map((headerGroup) => (
            <TableRow key={headerGroup.id}>
              {headerGroup.headers.map((header) => (
                <TableHead key={header.id}>
                  {header.isPlaceholder
                    ? null
                    : flexRender(
                        header.column.columnDef.header,
                        header.getContext()
                      )}
                </TableHead>
              ))}
            </TableRow>
          ))}
        </TableHeader>
        <TableBody>
          {table.getRowModel().rows?.length ? (
            table.getRowModel().rows.map((row) => (
              <TableRow key={row.id}>
                {row.getVisibleCells().map((cell) => (
                  <TableCell key={cell.id}>
                    {flexRender(cell.column.columnDef.cell, cell.getContext())}
                  </TableCell>
                ))}
              </TableRow>
            ))
          ) : (
            <TableRow>
              <TableCell colSpan={columns.length} className="h-24 text-center">
                No results.
              </TableCell>
            </TableRow>
          )}
        </TableBody>
      </Table>
    </div>
  )
}
export default Main